static VALUE rescue_any(VALUE data, VALUE e) {
  (void) data;
  (void) e;
  rb_eval_string(
      "  $stderr.print(\"#{$!.class}: #{$!.message}\\n\")\n"
      "  $!.backtrace.each do|frame|\n"
      "    $stderr.print(\"\\tfrom #{frame}\\n\")\n"
      "  end\n"
  );
  return Qnil;
}

